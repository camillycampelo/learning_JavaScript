// Operadores booleanos: Comparações

const num1 = 10;
const num2 = 20;

// Igualdade
console.log(num1 == num2);
console.log(num1 === num2);
console.log(num1 == '10');

// Desigualdade
console.log(num1 !== num2);

// Comparações
const idadePessoa1 = 25;
const idadePessoa2 = 30;
console.log(idadePessoa1 > idadePessoa2); // Maior que
console.log(idadePessoa1 < idadePessoa2); // Menor que
console.log(idadePessoa1 >= idadePessoa2); // Maior ou igual que
console.log(idadePessoa1 <= idadePessoa2); // Menor ou igual que
